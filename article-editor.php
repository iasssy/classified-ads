<h2>۳ ـ زمان مناسب را برای عکس گرفتن انتخاب کنید.&nbsp;</h2>
<p>نور مستقیم خورشید و سایه حاصل از آن عکس را خراب می کند. نور خورشید در نیمه روز آفتابی مناسب عکاسی نیست. بهترین زمان صبح ها و عصر ها قبل از غروب خورشید است ، زمانی که نور خورشید مایل می تابد و این باعث می شود رنگ ماشین بهتر در عکس دیده شود.&nbsp;</p>
<img class="article-image-bigger" src="images/blog/777-2.png" />
<h2>۴ - همیشه عکس آزمایشی بگیرید.&nbsp;</h2>
<p>دوربین های قدیمی دیدن بلافاصله عکسی که گرفته اید امکان نداشت ولی امروزه دوربین های دیجیتال این مشکل را حل کرده اند. پس از گرفتن عکس می توان آن را روی مونیتور دوربین دیجیتال و یا گوشی هوشمند دید و بررسی کرد.&nbsp;</p>
<h2>۵ - چرخاندن لاستیک خودرو به اطراف</h2>
<p>سعی کنید لاستیک خودرو را به جهتی که از آن &nbsp;عکس می گیرید بچرخانید، با اینکار لاستیک ها زاویه خوبی پیدا می کنند و خوب دیده می شوند.&nbsp;</p>
<img class="article-image-bigger" src="images/blog/228-4.png" />
<h2>۶ - مراقب سایه ها باشید.&nbsp;</h2>
<p>سایه ها می توانند عکس را خراب کنند. در عکس بالا یک سایه بر روی ماشین افتاده است و کار را خراب کرده است. علاوه بر آن اشیا جلویی مانع دید خودرو که عقب تر قرار گرفته می شوند. اجازه این اشتباه را ندهید و مراقب باشید تا سایه اضافی در کادر نباشد.&nbsp;</p>
<img class="article-image-bigger"  src="images/blog/888-1.png" />
<h2>۷ - یک یا دو قدم جلوتر بروید.&nbsp;</h2>
<p>با نزدیک شدن به ماشین عکس از فضای پشتی و غیرضروری رها می شود. البته که باید ماشین کاملا در کادر جا شود و سعی کنید به جز ماشین چیز دیگری در کادر نباشد.&nbsp;</p>
<p>در صورت نیاز قبل از انتشار عکس در آگهی خود آن را با یک نرم افزار مربوط به عکس ویرایش کنید. قسمت های اضافی را از عکس حذف کنید. همان طور که در بالا عکسی که ویرایش شده بهتر از دیگری است.&nbsp;</p>
<img class="article-image-biggest" src="images/blog/333-2.png"/>
<h2>۸ - روی نقاط قوت ماشین بیش&zwnj;تر تمرکز کنید.&nbsp;</h2>
<p>در عکس بالا روی این خودرو تکنولوژی هوشمند پارک خودرو نصب شده است. نور خیره کننده آن توجه هر بیننده ای را به خود جلب می کند و داشتن این تکنولوژی یک مزیت بزرگ است. گرفتن عکس از ویژگی های خاصی که ماشین شما دارد بازدید کننده را به خرید، بیش&zwnj;تر ترغیب می کند.&nbsp;</p>
<img class="article-image-bigger" src="images/blog/229-2.png" />
<h2>۹ - تمیز بودن بدنه و مرتب بودن داخل اتاق</h2>
<p>یکی از اشتباهات بزرگ هنگام عکس از ماشین مرتب نبودن داخل اتاق خودرو است. عکسی از داخل ماشین در حالی که تمیز و مرتب نیست تاثیر منفی روی بازکننده آگهی دارد.&nbsp;</p>
