<h2>نتیجه گیری:</h2>
<p>برای آگهی کردن و فروش سریعتر ماشین در اینترنت نیاز به عکس با کیفیت از ماشین است . با رعایت نکات بالا می توان عکس های خوبی از ماشین تهیه کرده و آگهی خود را متمایز کرد و نظر بازدید کننده بیش‌تری را به آگهی خود جلب کرد.</p>


